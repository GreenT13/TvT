<h1>Taaltraining op maat</h1>
De training is afgestemd op uw wensen en verwachtingen. We werken hard en we werken met afwisselende werkvormen.<br><br>

<h1>Nederlands op de werkvloer</h1>
De werksituatie van de cursist staat centraal. Dit betekent dat we werken met documenten die gebruikt worden op de werkplek en dat we bijvoorbeeld feedback geven op opnames van ‘echte’ telefoongesprekken. Ook krijgt de cursist opdrachten die gerelateerd zijn aan de werkzaamheden. De training wordt afgesloten met een assessment op maat waarbij de werkgever ook aanwezig is.<br /><br >

<h1>Training via Skype</h1>
Woont u niet in de Haagse regio, of woont u in het buitenland? U kunt de training ook via Skype volgen. Vanuit huis en via de computer heeft u direct contact met uw docent.<br><br>

