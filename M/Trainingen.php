Wilt u uw Nederlandse taalvaardigheid verbeteren? Bent u een inburgeraar die examen gaat doen? Wilt u schrijven zonder spelfouten? Talent voor Taal verzorgt privétrainingen op maat. <br><br>

<h1>Voorbeelden van trainingen</h1>
Een Poolse privécursist wil graag haar taalvaardigheid verbeteren zodat ze beter kan spreken met de juf op school van haar kind of een Russische manager die uitstekend kan spreken maar vindt dat zijn schrijfvaardigheid in zijn e-mails moet verbeteren. Ook geef ik les aan inburgeraars die inburgeringsexamen of staatsexamen willen doen of medewerkers van een bedrijf die beter moeten kunnen communiceren op de werkvloer.<br /><br />

